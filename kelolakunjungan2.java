    public final void loadData() {
        String SUrl = "jdbc:MySQL://localhost:3306/db_perpus";
        String SUser = "root";
        String SPass = "";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();
            DefaultTableModel model = new DefaultTableModel(new String[]{"Nama", "Prodi/Dosen", "NIM/NIDN", "Tujuan", "Tanggal", "Tingkat"}, 0);
            tabelkunjungan.setModel(model);
            String sql = "SELECT * FROM kunjungan";
            java.sql.ResultSet rs = st.executeQuery(sql);
            String nama, prodi, nim, tujuan, tanggal, tingkat;
            while (rs.next()) {
                nama = rs.getString("nama");
                prodi = rs.getString("prodi");
                nim = rs.getString("nim");
                tujuan = rs.getString("tujuan");
                tanggal = rs.getString("tanggal");
                tingkat = rs.getString("tingkat");
                model.addRow(new Object[]{nama, prodi, nim, tujuan, tanggal, tingkat});
            }
        } catch (Exception e) {
            System.out.println("Error " + e.getMessage());
        }
    }
