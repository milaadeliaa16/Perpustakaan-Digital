    private void updateData() {
        String dbUrl = "jdbc:mysql://localhost:3306/db_perpus";
        String dbUser = "root";
        String dbPass = "";

        String sql = "UPDATE kunjungan SET nama=?, prodi=?, tujuan=?, tanggal=?, tingkat=? WHERE nim=?";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(dbUrl, dbUser, dbPass);

            // Validate form fields
            if (nama.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Nama harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (prodi.getSelectedItem() == null || prodi.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Prodi harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (nim.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "NIM harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (tujuan.getSelectedItem() == null || tujuan.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Tujuan harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (tanggal.getDate() == null) {
                JOptionPane.showMessageDialog(new JFrame(), "Tanggal harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (tingkat.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Tingkat harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
                // Update the database record
                try (PreparedStatement stmt = conn.prepareStatement(sql)) {
                    stmt.setString(1, nama.getText());
                    stmt.setString(2, prodi.getSelectedItem().toString());
                    stmt.setString(3, tujuan.getSelectedItem().toString());
                    stmt.setDate(4, new java.sql.Date(tanggal.getDate().getTime())); // Convert to java.sql.Date
                    stmt.setString(5, tingkat.getText());
                    stmt.setString(6, nim.getText());

                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");

                    // Reset form fields after successful update
                    nama.setText("");
                    prodi.setSelectedIndex(-1);
                    nim.setText("");
                    tujuan.setSelectedIndex(-1);
                    tanggal.setDate(null);
                    tingkat.setText("");
                    showPanel();
                    loadData();  // Reload the table data to reflect the updates
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
                } finally {
                    conn.close();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Error! " + e.getMessage());
        }
    }

    private void hapusData() {
        int selectedRow = tabelkunjungan.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah yakin ingin menghapus data?",
                "Konfirmasi Hapus Data", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String nama = (String) tabelkunjungan.getValueAt(selectedRow, 0);
            String dbUrl = "jdbc:mysql://localhost:3306/db_perpus";
            String dbUser = "root";
            String dbPass = "";

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection(dbUrl, dbUser, dbPass);
                String sql = "DELETE FROM kunjungan WHERE nama = ?";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, nama);
                int rowsAffected = pst.executeUpdate();
                if (rowsAffected > 0) {
                    ((DefaultTableModel) tabelkunjungan.getModel()).removeRow(selectedRow);
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data", "Error", JOptionPane.ERROR_MESSAGE);
                }
                pst.close();
                con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

