    private void updateData() {
        String dbUrl = "jdbc:mysql://localhost:3306/db_perpus";
        String dbUser = "root";
        String dbPass = "";

        String sql = "UPDATE pegawai SET nama=?, email=?, divisi=? WHERE nidn=?";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(dbUrl, dbUser, dbPass);

            if ("".equals(nidn.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "NIDN harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if ("".equals(nama.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Nama harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if ("".equals(email.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Email harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if ("".equals(divisi.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Divisi harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
                try (PreparedStatement stmt = conn.prepareStatement(sql)) {

                    
                    stmt.setString(1, nama.getText());
                    stmt.setString(2, email.getText());
                    stmt.setString(3, divisi.getText());
                    stmt.setString(4, nidn.getText());

                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
                    nidn.setText("");
                    nama.setText("");
                    email.setText("");
                    divisi.setText("");
                    showPanel();
                    loadData();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
                } finally {
                    conn.close();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Error! " + e.getMessage());
        }

    }

    private void hapusData() {
        int selectedRow = tabelpegawai.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah yakin ingin menghapus data?",
                "Konfirmasi Hapus Data", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String idBuku = (String) tabelpegawai.getValueAt(selectedRow, 0);
            String dbUrl = "jdbc:mysql://localhost:3306/db_perpus";
            String dbUser = "root";
            String dbPass = "";

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection(dbUrl, dbUser, dbPass);
                String sql = "DELETE FROM pegawai WHERE nidn = ?";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, idBuku);
                int rowsAffected = pst.executeUpdate();
                if (rowsAffected > 0) {
                    ((DefaultTableModel) tabelpegawai.getModel()).removeRow(selectedRow);
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data", "Error", JOptionPane.ERROR_MESSAGE);
                }
                pst.close();
                con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

