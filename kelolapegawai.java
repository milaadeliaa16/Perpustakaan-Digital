    private void tambahdata() {
        String dbUrl = "jdbc:mysql://localhost:3306/db_perpus";
        String dbUser = "root";
        String dbPass = "";

        String sql = "INSERT INTO pegawai (nidn, nama, email, divisi) VALUES (?, ?, ?, ?)";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(dbUrl, dbUser, dbPass);

            if ("".equals(nidn.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "NIDN harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if ("".equals(nama.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Nama harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if ("".equals(email.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Email harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } else if ("".equals(divisi.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Divisi harus diisi", "Error", JOptionPane.WARNING_MESSAGE);
            } 
            else {
                try (PreparedStatement stmt = conn.prepareStatement(sql)) {
                    stmt.setString(1, nidn.getText());
                    stmt.setString(2, nama.getText());
                    stmt.setString(3, email.getText());
                    stmt.setString(4, divisi.getText());

                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
                    nidn.setText("");
                    nama.setText("");
                    email.setText("");
                    divisi.setText("");
                    showPanel();
                    loadData();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
                } finally {
                    conn.close();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Error! " + e.getMessage());
        }
    }

