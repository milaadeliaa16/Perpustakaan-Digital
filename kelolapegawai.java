    public final void loadData() {
        btnHapus.setVisible(false);
        btnCancel.setVisible(false);
        String SUrl, SUser, SPass;
        SUrl = "jdbc:MySQL://localhost:3306/db_perpus";
        SUser = "root";
        SPass = "";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();
            DefaultTableModel model = new DefaultTableModel(new String[]{"NIDN", "Nama Lengkap", "Email", "Divisi"}, 0);
            tabelpegawai.setModel(model);
            String sql = "SELECT * FROM pegawai";
            java.sql.ResultSet rs = st.executeQuery(sql);
            String nidn,nama,email,divisi;
            while (rs.next()) {
                nidn = rs.getString("nidn");
                nama = rs.getString("nama");
                email = rs.getString("email");
                divisi = rs.getString("divisi");
                model.addRow(new Object[]{nidn, nama, email, divisi});
            }
        } catch (Exception e) {
            System.out.println("Error " + e.getMessage());
        }
    }
